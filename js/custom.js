function addition(x,y){
    var sum = x + y;
    document.getElementsByClassName('result')[0].innerHTML = sum;
}

function subtraction(x,y){
    var sub = x - y;
    document.getElementsByClassName('result')[1].innerHTML = sub;

}
function multiplication(x,y){
    var mlt = x * y;
    document.getElementsByClassName('result')[2].innerHTML = mlt;

}